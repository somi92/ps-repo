    public void popuniComboBox() {
        jcomboPP.removeAllItems();
        if(ppartneri.isEmpty()) {
            jcomboPP.addItem("Nema poslovnih partnera u bazi.");
            return;
        }
        for(PoslovniPartner pp : ppartneri) {
            jcomboPP.addItem(pp);
        }
    }
    
