    private void jbtnDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDetaljiActionPerformed
        if(ppartneri.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ne postoje podaci o poslovnim partnerima u bazi.", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);
        } else {
            setVisible(false);
            FPoslovniPartner fpp = new FPoslovniPartner(this, new AzurirajOperacija(new Operacije()), (PoslovniPartner) jcomboPP.getSelectedItem());
            fpp.setVisible(true);
        }
    }//GEN-LAST:event_jbtnDetaljiActionPerformed

